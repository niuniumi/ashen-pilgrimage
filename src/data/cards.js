const CARD_ALIASES = {
  'nun-confession': 'nun-confession-mark'
};

export function canonicalCardId(cardId) {
  return CARD_ALIASES[cardId] ?? cardId;
}

export function findCard(cardId) {
  const id = canonicalCardId(cardId);
  return cards.find((card) => card.id === id) ?? null;
}

export function getCard(cardId) {
  return findCard(cardId) ?? cards[0];
}

export function isKnownCardId(cardId) {
  return Boolean(findCard(cardId));
}

export function getPlayableRewardCards(characterId) {
  return cards.filter((card) => !card.unplayable && (card.character === characterId || card.character === 'common'));
}
